package bai11;

public class TwoSoPhuc {
    private double phanThuc;
    private double phanAo;

    private double phanThuc2;

    private double phanAo2;

    public TwoSoPhuc() {
    }

    public TwoSoPhuc(double phanThuc, double phanAo) {
        this.phanThuc = phanThuc;
        this.phanAo = phanAo;
    }

    public double getPhanThuc() {
        return phanThuc;
    }

    public void setPhanThuc(double phanThuc) {
        this.phanThuc = phanThuc;
    }

    public double getPhanAo() {
        return phanAo;
    }

    public void setPhanAo(double phanAo) {
        this.phanAo = phanAo;
    }

    public double getPhanThuc2() {
        return phanThuc2;
    }

    public void setPhanThuc2(double phanThuc2) {
        this.phanThuc2 = phanThuc2;
    }

    public double getPhanAo2() {
        return phanAo2;
    }

    public void setPhanAo2(double phanAo2) {
        this.phanAo2 = phanAo2;
    }
}
